#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;
	char	*d;
	char	*s;

	d = (char *)dst;
	s = (char *)src;
	i = 0;
	while (i < n && (s + 1) != NULL)
	{
		d[i] = (unsigned char)s[i];
		i++;
	}
	return (dst);
}
