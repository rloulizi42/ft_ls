int		ft_atoi2(const char *str)
{
	int		result;
	int		i;
	int		p;

	i = 0;
	p = 1;
	result = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '1')
			result = result + p;
		i++;
		p = p * 2;
	}
	return (result);
}
